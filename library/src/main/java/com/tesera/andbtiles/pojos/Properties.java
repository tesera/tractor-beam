package com.tesera.andbtiles.pojos;

public class Properties {

    private String description;
    private String id;
    private String marker_color;
    private String marker_size;
    private String marker_symbol;
    private String title;

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMarker_color() {
        return marker_color;
    }

    public void setMarker_color(String marker_color) {
        this.marker_color = marker_color;
    }

    public String getMarker_size() {
        return marker_size;
    }

    public void setMarker_size(String marker_size) {
        this.marker_size = marker_size;
    }

    public String getMarker_symbol() {
        return marker_symbol;
    }

    public void setMarker_symbol(String marker_symbol) {
        this.marker_symbol = marker_symbol;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

}
